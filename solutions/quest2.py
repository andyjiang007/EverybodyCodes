def solve1(input, dict):
    result = 0
    wordList = input.split(" ")
    for word in wordList:
        for dictWord in dict:
            if dictWord in word:
                result += 1
    return result


def solve2(input, dict):
    result = 0
    for line in input:
        wordList = line.split(" ")
        for word in wordList:
            runicChar = set()
            for dictWord in dict:
                dictWordLen = len(dictWord)
                for i in range(len(word) - dictWordLen + 1):
                    # print(word[i : i + dictWordLen])
                    if word[i : i + dictWordLen] == dictWord:
                        runicChar |= set(range(i, i + dictWordLen))
                reverseWord = word[::-1]
                for i in range(len(reverseWord) - dictWordLen + 1):
                    # print(reverseWord[i : i + dictWordLen])
                    if reverseWord[i : i + dictWordLen] == dictWord:
                        runicChar |= set(
                            range(len(word) - i - dictWordLen, len(word) - i)
                        )
            # print(runicChar)
            result += len(runicChar)
    return result


