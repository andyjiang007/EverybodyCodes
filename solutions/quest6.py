from collections import deque


class Node:
    def __init__(self, value):
        self.value = value  # The value of the node
        self.children = []  # List to hold child nodes

    def add_child(self, child_node):
        """Adds a child to the current node."""
        self.children.append(child_node)

    def __repr__(self):
        return f"Node({self.value}), children: {self.children}"


def solve1(input):
    result = 0
    root = Node("RR")
    allNodes = {}
    allNodes["RR"] = root
    inputArray = [line for line in input.split("\n") if line]
    for line in inputArray:
        curNode, leafNodes = line.split(":")
        if curNode not in allNodes:
            newNode = Node(curNode)
            allNodes[curNode] = newNode
        # print(leafNodes)
        leaves = leafNodes.split(",")

        for leaf in leaves:
            if leaf not in allNodes:
                newNode = Node(leaf)
                allNodes[leaf] = newNode
            allNodes[curNode].add_child(allNodes[leaf])
    print(allNodes)

    allPaths = []
    queue = deque([(root, [root.value])])
    while len(queue) > 0:
        current, path = queue.popleft()
        if current.value == "BUG" or current.value == "ANT":
            continue

        # If the current node has no children, it's a leaf
        if not current.children and "@" in path:
            allPaths.append(("->".join(map(str, path))))

        # Add children to the queue with the updated path
        for child in current.children:
            queue.append((child, path + [child.value]))
    print(allPaths)

    def countDepth(path):
        return len(path.split("->"))

    pathLengths = list(map(countDepth, allPaths))
    print(pathLengths)
    length_counts = {}
    for length in pathLengths:
        if length in length_counts:
            length_counts[length] += 1
        else:
            length_counts[length] = 1
    unique_length = None
    for length, count in length_counts.items():
        if count == 1:
            unique_length = length
            break
    print(unique_length)
    if unique_length is not None:
        for s in allPaths:
            if countDepth(s) == unique_length:
                return s

    return result


